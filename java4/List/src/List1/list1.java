package List1;

import java.util.ArrayList;
import java.util.Arrays;

public class list1 {

	public static void main(String[] args) {
		// add(추가), get(데이터로드), remove(삭제), size(index 개수) : ArrayList라는 uill 메소드에서 사용하는
		// 명령어
		// Arrays.asList: 로드할 배열 변수를 적용
		// 중요: ADD는 일반적으로 무조건 맨 뒤에 데이터가 추가 됨 단, index번호를 적용 후 값을 실행하면
		// 해당 index부분에 추가가 가능.

		String member[] = { "이순신", "홍길동", "유관순", "강감찬" };
		ArrayList<String> mb = new ArrayList<>(Arrays.asList(member));
		// System.out.println(mb);
		int ea = mb.size();
		// System.out.println(ea);
		mb.add("김유신");
		// System.out.println(mb);
		mb.remove(1);
		// System.out.println(mb);
		mb.add(3, "세종대왕");
		// System.out.println(mb);
		String checks = mb.get(2);
		// System.out.println(checks);
		
		/*
		 * 응용문제
		 * 본 데이터를 커스텀하여 다음 결과처럼 출력하시오.
		 * 결과 - [7,15,22,8,11,39,41]
		 */
		// ArrayList에는 int를 사용하지 않고 integer를 대신 사용함.
		Integer numbers[] = {15,22,37,8,11,19,41};
		ArrayList<Integer> nb = new ArrayList<>(Arrays.asList(numbers));
		System.out.println(nb);
		nb.remove(5);
		nb.remove(2);
		nb.add(4,39);
		nb.add(0,7);
		System.out.println(nb);
		
		
	}

}
