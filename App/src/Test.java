public class Test { //class란 가장 기본이 되는 전체이름 명칭을 말함.

	// main함수는 자바실행시 가장 먼저 실행되는 프로그램
	public static void main(String[] args) { //함수 = 뭔가를 실행하기 위해 하는 행동.
	/* String = 문자
	 [] = 배열
	 String[] = 문자열
	 args = 단순 변수명 
	 system.out.println = 자바의 결과값을 출력할 때 사용하는 함수입니다.
	 system.out.print = Println과 기능은 동일하나 모든 결과 값을 한 줄로만 표현함.
	*/
		System.out.println("홍길동님 환영합니다."); //sysout -> ctrl+space
		System.out.println("적립금은 2000원 입니다."); //ln(line)은 enter 기능.
		System.out.print("등급은 실버등급입니다.");
		System.out.print("등급은 실버등급입니다.");
	}

}
